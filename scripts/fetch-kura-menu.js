const fs = require("fs");
const path = require("path");
const axios = require("axios");
const cheerio = require("cheerio");

const URL = "https://www.kurasushi.co.jp/menu/?area=area0";

const CATEGORY_BY_ID = {
  menu01: "おすすめ",
  menu02: "にぎり",
  menu03: "にぎり一貫",
  menu04: "ぐんかん・細巻",
  menu05: "竹姫寿司",
  menu06: "糖質オフシリーズ",
  menu07: "サイドメニュー",
  menu08: "デザート",
};

function cleanText(value) {
  return String(value || "")
    .replace(/\s+/g, " ")
    .trim();
}

function getCategory($, item) {
  const section = item.closest("section");
  const sectionId = section.attr("id");

  if (sectionId && CATEGORY_BY_ID[sectionId]) {
    return CATEGORY_BY_ID[sectionId];
  }

  const headerText = cleanText(
    section
      .find(".menu-section-header, h2, h3")
      .first()
      .text()
  );

  if (headerText) {
    return headerText;
  }

  return "その他";
}

async function fetchMenu() {
  console.log("くら寿司メニュー取得開始...");
  console.log("取得元:", URL);

  const { data } = await axios.get(URL, {
    timeout: 30000,
    headers: {
      "User-Agent":
        "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome Safari",
      "Accept-Language": "ja,en-US;q=0.9,en;q=0.8",
    },
  });

  const $ = cheerio.load(data);
  const items = [];

  $(".menu-item").each((_, el) => {
    const item = $(el);

    const name = cleanText(
      item.find(".menu-name").first().text() ||
        item.find("h4").first().text() ||
        item.find("img").first().attr("alt")
    );

    if (!name) return;

    const text = cleanText(item.text());

    const priceMatch = text.match(/(\d{2,4})\s*円/);
    const kcalMatch = text.match(/([0-9.]+)\s*kcal/i);

    const price = priceMatch ? Number(priceMatch[1]) : null;
    const calories = kcalMatch ? Number(kcalMatch[1]) : null;
    const category = getCategory($, item);

    items.push({
      id: items.length + 1,
      name,
      price,
      calories,
      category,
    });
  });

  const uniqueItems = [];
  const seen = new Set();

  for (const item of items) {
    const key = [item.name, item.price, item.calories, item.category].join("|");

    if (seen.has(key)) continue;

    seen.add(key);
    uniqueItems.push({
      ...item,
      id: uniqueItems.length + 1,
    });
  }

  if (uniqueItems.length < 10) {
    const debugPath = path.join(__dirname, "../debug-kura.html");
    fs.writeFileSync(debugPath, data, "utf8");

    throw new Error(
      `抽出件数が少なすぎます: ${uniqueItems.length}。debug-kura.html を出力しました。`
    );
  }

  const output =
    "// This file is auto-generated by scripts/fetch-kura-menu.js\n" +
    `// Updated at: ${new Date().toISOString()}\n` +
    "const KURA_MENU = " +
    JSON.stringify(uniqueItems, null, 2) +
    ";\n";

  const outputPath = path.join(__dirname, "../menu-data.js");
  fs.writeFileSync(outputPath, output, "utf8");

  console.log("取得完了！");
  console.log(`商品数: ${uniqueItems.length}`);
  console.log("出力先:", outputPath);
}

fetchMenu().catch((error) => {
  console.error("エラー:", error.message);
  process.exit(1);
});
